// Regenerates the package-derived config that would otherwise be edited by hand:
//   - the workflow_dispatch `package` choice list in cd-packages.yml (non-private packages)
//   - the .codecov.yml flag + component entries for each package (all packages)
//   - the "Affected area" dropdown options in every .github/ISSUE_TEMPLATE form (all packages)
// Run `pnpm gen:publish-options` to update them; the pre-commit hook runs it with --check
// to fail a commit when the committed config has drifted from packages/ on disk. Each block
// lives between `# gen:...` marker comments so hand-authored siblings (api, theme) are kept.
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..')
const packagesDir = join(repoRoot, 'packages')

const allPackages = readdirSync(packagesDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .filter((name) => existsSync(join(packagesDir, name, 'package.json')))
  .sort()

const publishable = allPackages.filter(
  (name) =>
    JSON.parse(readFileSync(join(packagesDir, name, 'package.json'), 'utf8')).private !== true
)

if (publishable.length === 0) {
  console.error(
    'No publishable packages found under packages/; refusing to emit an empty options list.'
  )
  process.exit(1)
}

const NOTE = '— regenerated by `pnpm gen:publish-options`; do not edit by hand'

// Every GitHub issue *form* carries an identical "Affected area" dropdown whose
// package options live between `# gen:issue-areas` markers (the `web` option is
// hand-managed above them). config.yml is the chooser config, not a form — skip it.
const issueTemplateDir = join(repoRoot, '.github', 'ISSUE_TEMPLATE')
const issueTemplates = [
  '1.bug_report.yml',
  '2.feature_request.yml',
  '3.rfc.yml',
  '4.epic.yml',
  '5.user_story.yml',
  '6.task.yml',
  '7.documentation_feedback.yml',
]

// Each region is a marker pair plus the lines rendered between them (unindented — the
// marker's own indentation is captured from the file and prefixed to every line).
const edits = [
  {
    path: join(repoRoot, '.github', 'workflows', 'cd-packages.yml'),
    regions: [
      {
        start: '# gen:publish-options start',
        end: '# gen:publish-options end',
        lines: ['options:', ...publishable.map((name) => `  - ${name}`)],
      },
    ],
  },
  {
    path: join(repoRoot, '.codecov.yml'),
    regions: [
      {
        start: '# gen:codecov-flags start',
        end: '# gen:codecov-flags end',
        lines: allPackages.flatMap((name) => [
          `- name: ${name}`,
          '  paths:',
          `    - packages/${name}/`,
        ]),
      },
      {
        // Package flags in the patch-status gate (coverage.status.patch.default.flags).
        // The app/worker flags (web, e2e, api) are hand-managed above these markers.
        start: '# gen:codecov-patch-flags start',
        end: '# gen:codecov-patch-flags end',
        lines: allPackages.map((name) => `- ${name}`),
      },
      {
        start: '# gen:codecov-components start',
        end: '# gen:codecov-components end',
        lines: allPackages.flatMap((name) => [
          `- component_id: ${name}`,
          `  name: ${name}`,
          '  paths:',
          `    - packages/${name}/**`,
        ]),
      },
    ],
  },
  ...issueTemplates.map((file) => ({
    path: join(issueTemplateDir, file),
    regions: [
      {
        // Package options in the "Affected area" dropdown; `web` is hand-managed above the markers.
        start: '# gen:issue-areas start',
        end: '# gen:issue-areas end',
        lines: allPackages.map((name) => `- ${name}`),
      },
    ],
  })),
  {
    // The label-area workflow's ALLOWED_AREAS guard — same package set, plain names
    // (no `- ` list prefix) since it lives in a YAML block scalar, not a sequence.
    path: join(repoRoot, '.github', 'workflows', 'label-area.yml'),
    regions: [
      {
        start: '# gen:issue-areas start',
        end: '# gen:issue-areas end',
        lines: allPackages.map((name) => name),
      },
    ],
  },
]

const escape = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, String.raw`\$&`)

function applyRegion(source, { start, end, lines }, path) {
  const region = new RegExp(
    String.raw`([ \t]*)${escape(start)}[^\n]*\n[\s\S]*?\n[ \t]*${escape(end)}[^\n]*`
  )
  const match = region.exec(source)
  if (!match) {
    console.error(`Markers "${start}" … "${end}" not found in ${path}`)
    process.exit(1)
  }
  const indent = match[1]
  const block = [
    `${indent}${start} ${NOTE}`,
    ...lines.map((line) => `${indent}${line}`),
    `${indent}${end}`,
  ].join('\n')
  return source.replace(region, block)
}

const check = process.argv.includes('--check')
let stale = false

for (const { path, regions } of edits) {
  const source = readFileSync(path, 'utf8')
  let updated = source
  for (const region of regions) updated = applyRegion(updated, region, path)
  if (updated === source) continue
  stale = true
  if (!check) writeFileSync(path, updated)
}

if (check) {
  if (stale) {
    console.error('Generated package config is stale. Run `pnpm gen:publish-options` and commit.')
    process.exit(1)
  }
  console.log('Generated package config is up to date.')
} else if (stale) {
  console.log(`Synced package config for ${allPackages.length} packages.`)
} else {
  console.log('Generated package config already up to date.')
}
